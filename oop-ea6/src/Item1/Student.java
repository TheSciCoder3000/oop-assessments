package Item1;

public class Student {
	private String stdName;
	private int stdAge;
	
	public void setName(String set_name) {
		stdName = set_name;
	}
	
	public String getName() {
		return stdName;
	}
	
	public void setAge(int set_age) {
		stdAge = set_age;
	}
	
	public int getAge() {
		return stdAge;
	}
}
